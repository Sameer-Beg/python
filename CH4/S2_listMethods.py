frnds = ["Ravi", "Raju", "Rahul", "Rajesh", "Ramesh"]
print(frnds)
frnds.append("Sameer")  #append method adds the element at the end of the list
print(frnds)

l1 = [2,3,4,5,698,87]
l1.sort()  #sort method sorts the list in ascending order
print(l1)
l1.reverse()  #reverse method reverses the list
print(l1)   

l2 = ["Ravi", "Raju", "Rahul", "Rajesh", "Ramesh"]
l2.insert(2, "Sameer")  #insert method inserts the element at the given index
print(l2)

l3  = ["Ravi", "Raju", "Rahul", "Rajesh", "Ramesh" ]
l3.pop(3)  #pop method removes the last element of the list
print(l3)
l3.remove("Raju")  #remove method removes the given element from the list
print(l3)