s = {1,2,3,4,5,6}
print(s.remove(3))  # it will remove the element mentioned
print(s)
print(len(s))  # it will give the length of set
print(s.pop())  # it will remove the last element of set
print(s)
print(s.clear())  # it will clear the set and make it empty
print(s)


